#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;
	char	*str;

	i = 0;
	str = (char *)s;
	while (n)
	{
		str[i] = (char)c;
		i++;
		n--;
	}
	return (str);
}
